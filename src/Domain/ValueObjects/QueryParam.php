<?php

namespace Crypto\Domain\ValueObjects;

class QueryParam
{
    private string $value;
    private string $type;

    public function __construct(string $type, string $value)
    {
        $this->type = $type;
        $this->value = $value;
    }

    public function getType(): string
    {
        return $this->type;
    }

    public function getValue(): string
    {
        return $this->value;
    }
}
